export function getWidth(game) {
    return game.config.width;
}

export function getHeight(game) {
    return game.config.height;
}

export function getCenterX(game) {
    return game.config.width * 0.5;
}

export function getCenterY(game) {
    return game.config.height * 0.5;
}

export function getViewport(game) {
    return game.config.viewport;
}


